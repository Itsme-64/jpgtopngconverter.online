	<script>
  // Automatically submit the form when a file is selected
  function submitForm() {
    document.getElementById('fileUploadForm').submit();
    
    var fileInput = document.getElementById('fileToUpload');
    var label = document.getElementById('fileLabel');

    if (fileInput.files.length > 0) {
        var fileName = fileInput.files[0].name;

        if (fileName.length > 15) {
            fileName = fileName.substring(0, 12) + "...";
        }

        label.textContent = "Selected: " + fileName;
    } else {
        label.textContent = "Select JPG File"; // Reset if no file is selected
    }
  }
  
  function updateFileName() {
    var fileInput = document.getElementById('filesToUpload');
    var fileNames = Array.from(fileInput.files).map(file => file.name);
    
    // Join file names and limit total length to 50 characters
    var fileDisplay = fileNames.join(', ');
    if (fileDisplay.length > 50) {
      fileDisplay = fileDisplay.substring(0, 47) + '...';
    }
    
    // Update the label text
    document.querySelector('label[for="filesToUpload"]').textContent = 'Selected: ' + fileDisplay;
  }
